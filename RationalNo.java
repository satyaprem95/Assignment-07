package Assignment_07;
public class RationalNo {
    private int nume, denom;


    public RationalNo(int nume, int denom) {
        this.nume = nume;
        this.denom = denom;
    }

    public int getNume() {
        return nume;
    }

    public void setNume(int nume) {
        this.nume = nume;
    }

    public int getDenom() {
        return denom;
    }

    public void setDenom(int denom) {
        this.denom = denom;
    }
}

